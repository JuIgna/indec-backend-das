package ar.edu.ubp.das.indecback.beans;

public class IdiomaBean {
    private String cod_idioma;
    private String nom_idioma;

    public IdiomaBean() {}

    public String getCod_idioma() {
        return cod_idioma;
    }

    public void setCod_idioma(String cod_idioma) {
        this.cod_idioma = cod_idioma;
    }

    public String getNom_idioma() {
        return nom_idioma;
    }

    public void setNom_idioma(String nom_idioma) {
        this.nom_idioma = nom_idioma;
    }
}
